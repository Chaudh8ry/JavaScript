// --------------------------Arithemetic operators----------------------------
/*  
    '+' --> Addition
    '-' --> Subtraction
    '*' --> Multiplication
    '**' --> Exponentiation
    '/' --> Division
    '%' --> Modulus
    '++' --> Increment
    '--' --> Decrement
*/

let a = 45;
let b = 4;
console.log("a + b =",a+b);
console.log("a - b =",a-b);
console.log("a / b =",a/b);
console.log("a * b =",a*b);
console.log("a ** b =",a**b); // 'a' to the power 'b'
console.log("a % b =",a%b); // remainder = 1


// Increment operator
console.log("a++ =",a++); // output will be '45', pehle value upadte hogi fir next line mai show hogi
console.log(a); //output will be '46'

console.log("++a =",++a); // value saath saath update hoke print hojayegi
