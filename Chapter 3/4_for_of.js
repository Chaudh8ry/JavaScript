// Used to iterate over the values of iterable objects like arrays, strings, maps, etc.

// *Iterating over Strings
let message = "Hello World!";

for(let char of message){
    console.log(char); // output : H e l l o  W o r l d !
}

// *Iterating over Array
let colours = ["Red","Yellow","Blue"];
// iterating over each colour and printing it
for(let rang of colours){
    console.log(rang); // output: Red Yellow Green
}